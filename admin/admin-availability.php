    function handleQuickAction(action) {
        if (action === 'block-day' && selectedDate) {
            createBlock(selectedDate, defaultStart, defaultEnd, 'unavailable', 'Entire day blocked');
        } else if (action === 'block-day' && !selectedDate) {
            alert('Please select a day on the calendar first.');
        } else if (action === 'block-weekend') {
            var today = new Date();
            var dow = today.getDay();
            var daysToSat = (6 - dow) % 7 || 7;
            var sat = new Date(today); sat.setDate(today.getDate() + daysToSat);
            var sun = new Date(sat);   sun.setDate(sat.getDate() + 1);
            [sat, sun].forEach(function(d) {
                var ds = d.toISOString().split('T')[0];
                createBlock(ds, defaultStart, defaultEnd, 'personal', 'Weekend');
            });
        } else if (action === 'block-time-range') {
            if (selectedTimeSlots.length >= 2) {
                var st = selectedTimeSlots[0];
                var et = selectedTimeSlots[selectedTimeSlots.length - 1];
                var ep = et.split(':').map(Number);
                var nd = new Date(); nd.setHours(ep[0], ep[1] + 15, 0);
                var ets = String(nd.getHours()).padStart(2,'0') + ':' + String(nd.getMinutes()).padStart(2,'0');
                createBlock(selectedDate, st, ets, 'unavailable', 'Time range');
            } else {
                alert('Please select at least 2 time slots to create a time range.');
            }
        } else if (action === 'clear-selection') {
            clearSelection();
        }
    }

