    function renderBlocksList() {
        var html = '';
        var typeLabels = { 'unavailable':'Unavailable', 'maintenance':'Maintenance', 'personal':'Personal' };
        blocks.forEach(function(block) {
            var s = new Date(block.start_datetime.replace(' ','T'));
            var e = new Date(block.end_datetime.replace(' ','T'));
            html += '<div class="hp-block-item ' + block.block_type + '">'
                  + '<div class="hp-block-info">'
                  + '<strong>' + (typeLabels[block.block_type] || block.block_type) + '</strong>'
                  + '<small>' + s.toLocaleString() + ' &ndash; ' + e.toLocaleString() + '</small>'
                  + (block.reason ? '<small>Reason: ' + block.reason + '</small>' : '')
                  + '</div>'
                  + '<div class="hp-block-actions">'
                  + '<button class="button button-small hp-delete-block" data-id="' + block.id + '">Delete</button>'
                  + '</div></div>';
        });
        extensions.forEach(function(ex) {
            html += '<div class="hp-block-item extension">'
                  + '<div class="hp-block-info">'
                  + '<strong>Extended (unblocked)</strong>'
                  + '<small>' + ex.date + ' ' + ex.start_time + ' &ndash; ' + ex.end_time + '</small>'
                  + '</div>'
                  + '<div class="hp-block-actions">'
                  + '<button class="button button-small hp-remove-extension" data-date="' + ex.date + '" data-start="' + ex.start_time + '" data-end="' + ex.end_time + '">Remove</button>'
                  + '</div></div>';
        });
        if (!html) html = '<p>No active blocks or extended hours for this month.</p>';
        $('#hp-blocks-container').html(html);
        $('.hp-delete-block').on('click', function() {
            if (confirm('Delete this block?')) deleteBlock($(this).data('id'));
        });
        $('.hp-remove-extension').on('click', function() {
            var d = $(this).data('date'), st = $(this).data('start'), en = $(this).data('end');
            if (confirm('Remove this extended availability?')) removeExtension(d, st, en);
        });
    }

