    function saveBlock() {
        var st, et;
        if ($('#hp-use-custom-range').is(':checked')) {
            st = $('#hp-custom-start').val();
            et = $('#hp-custom-end').val();
            if (!st || !et) { alert('Please enter custom start and end times.'); return; }
            if (st >= et) { alert('End time must be after start time.'); return; }
        } else {
            if (!selectedDate || !selectedTimeSlots.length) {
                alert('Please select a date and at least one time slot.');
                return;
            }
            st = selectedTimeSlots[0];
            var ep = selectedTimeSlots[selectedTimeSlots.length - 1].split(':').map(Number);
            var interval = parseInt(timeSlotInterval, 10) || 15;
            var nd = new Date(); nd.setHours(ep[0], ep[1] + interval, 0);
            et = String(nd.getHours()).padStart(2,'0') + ':' + String(nd.getMinutes()).padStart(2,'0');
        }
        if (!selectedDate) { alert('Please select a date.'); return; }

        var dateExtensions = getExtensionsForDate(selectedDate);
        var allExtended = selectedTimeSlots.length && selectedTimeSlots.every(function(slot) {
            return isTimeDefaultBlocked(slot) && isTimeExtended(selectedDate, slot + ':00', dateExtensions);
        });

        if (allExtended) {
            var startDt = selectedDate + ' ' + (st.length === 5 ? st + ':00' : st);
            var endDt   = selectedDate + ' ' + (et.length === 5 ? et + ':00' : et);
            $.post(phpBookingAdmin.ajaxUrl, {
                action: 'hp_remove_extension',
                nonce: phpBookingAdmin.nonce,
                start_datetime: startDt,
                end_datetime: endDt
            }).done(function(r) {
                if (r.success) { alert('Extended time re-blocked.'); clearSelection(); loadMonthData(); }
                else { alert(r.data && r.data.message ? r.data.message : 'Error'); }
            }).fail(function() { alert('Request failed.'); });
        } else {
            $.post(phpBookingAdmin.ajaxUrl, {
                action: 'hp_block_availability',
                nonce: phpBookingAdmin.nonce,
                block_type: $('#hp-block-type').val(),
                start_datetime: selectedDate + ' ' + (st.length === 5 ? st + ':00' : st),
                end_datetime:   selectedDate + ' ' + (et.length === 5 ? et + ':00' : et),
                reason: $('#hp-block-reason').val() || ''
            }).done(function(r) {
                if (r.success) { alert('Time blocked!'); clearSelection(); loadMonthData(); }
                else { alert(r.data && r.data.message ? r.data.message : 'Error'); }
            }).fail(function() { alert('Request failed.'); });
        }
    }

    function unblockSelected() {
        if (!selectedDate || !selectedTimeSlots.length) {
            alert('Please select time slots to unblock.');
            return;
        }
        var st = selectedTimeSlots[0];
        var ep = selectedTimeSlots[selectedTimeSlots.length - 1].split(':').map(Number);
        var interval = parseInt(timeSlotInterval, 10) || 15;
        var nd = new Date(); nd.setHours(ep[0], ep[1] + interval, 0);
        var et = String(nd.getHours()).padStart(2,'0') + ':' + String(nd.getMinutes()).padStart(2,'0');
        var extendMode = $('#hp-unblock-btn').data('extend-mode');

        if (extendMode) {
            $.post(phpBookingAdmin.ajaxUrl, {
                action: 'hp_extend_availability',
                nonce: phpBookingAdmin.nonce,
                date: selectedDate,
                start_time: st,
                end_time: et
            }).done(function(r) {
                if (r.success) { alert('Times unblocked (extended availability)!'); clearSelection(); loadMonthData(); }
                else { alert(r.data && r.data.message ? r.data.message : 'Error'); }
            }).fail(function() { alert('Request failed.'); });
        } else {
            var startDt = selectedDate + ' ' + st + ':00';
            var endDt   = selectedDate + ' ' + et + ':00';
            $.post(phpBookingAdmin.ajaxUrl, {
                action: 'hp_unblock_by_range',
                nonce: phpBookingAdmin.nonce,
                start_datetime: startDt,
                end_datetime: endDt
            }).done(function(r) {
                if (r.success) { alert('Unblocked!'); clearSelection(); loadMonthData(); }
                else { alert(r.data && r.data.message ? r.data.message : 'Error'); }
            }).fail(function() { alert('Request failed.'); });
        }
    }

