<script>
jQuery(document).ready(function($) {
    var currentDate = new Date();
    var selectedDate = null;
    var selectedTimeSlots = [];
    var blocks = [];
    var extensions = [];
    var bookings = [];
    var dataReady = 0; // track when both async loads finish
    var isDragging = false;
    var dragMode = true; // true for selecting, false for deselecting
    var defaultStart = '<?php echo esc_js($default_start); ?>' || '08:00';
    var defaultEnd   = '<?php echo esc_js($default_end); ?>'   || '20:00';
    var timeSlotInterval = <?php echo (int) $time_slot_interval; ?> || 15;

    $(document).on('mouseup', function() { isDragging = false; });

    // ---- Bootstrap: load data, THEN render ----
    loadMonthData();

    // Navigation — reload data when month changes
    $('#hp-prev-month').on('click', function() {
        currentDate.setMonth(currentDate.getMonth() - 1);
        loadMonthData();
    });
    $('#hp-next-month').on('click', function() {
        currentDate.setMonth(currentDate.getMonth() + 1);
        loadMonthData();
    });

    // Quick Actions
    $('.hp-action-btn').on('click', function() { handleQuickAction($(this).data('action')); });

    // Block form and custom range
    $('#hp-block-form').on('submit', function(e) { e.preventDefault(); saveBlock(); });
    $('.hp-cancel-btn').on('click', function() { clearSelection(); });
    $('#hp-use-custom-range').on('change', function() { $('#hp-custom-range-wrap').toggle($(this).is(':checked')); });
    $('#hp-unblock-btn').on('click', function() { unblockSelected(); });
    $('#hp-apply-hours').on('click', function() { applyWorkingHours(false); });
    $('#hp-save-hours').on('click', function() { applyWorkingHours(true); });

    // =========================================================
    // Data loading — loads blocks + bookings, then re-renders
    // =========================================================
    function getMonthRange() {
        var y = currentDate.getFullYear(), m = currentDate.getMonth();
        var startDate = new Date(y, m, 1).toISOString().split('T')[0];
        var endDate   = new Date(y, m + 1, 0).toISOString().split('T')[0];
        return { start: startDate, end: endDate };
    }

    function loadMonthData() {
        dataReady = 0;
        var range = getMonthRange();

        // Load blocks
        $.get(phpBookingAdmin.ajaxUrl, {
            action: 'hp_get_availability_blocks',
            nonce: phpBookingAdmin.nonce,
            start_date: range.start,
            end_date: range.end
        }).done(function(r) {
            blocks = (r.success && r.data.blocks) ? r.data.blocks : [];
            extensions = (r.success && r.data.extensions) ? r.data.extensions : [];
            renderBlocksList();
            if (++dataReady >= 2) renderCalendar();
        }).fail(function() {
            blocks = [];
            extensions = [];
            if (++dataReady >= 2) renderCalendar();
        });

        // Load bookings for the entire month (date range)
        $.get(phpBookingAdmin.ajaxUrl, {
            action: 'hp_get_bookings',
            nonce: phpBookingAdmin.nonce,
            start_date: range.start,
            end_date: range.end,
            per_page: 200
        }).done(function(r) {
            bookings = (r.success && r.data.bookings) ? r.data.bookings : [];
            if (++dataReady >= 2) renderCalendar();
        }).fail(function() {
            bookings = [];
            if (++dataReady >= 2) renderCalendar();
        });

        // Render immediately with stale data so user sees the calendar grid
        renderCalendar();
    }

    // =========================================================
    // Calendar rendering
    // =========================================================
    function renderCalendar() {
        var year = currentDate.getFullYear(), month = currentDate.getMonth();
        var daysInMonth = new Date(year, month + 1, 0).getDate();
        var startingDay = new Date(year, month, 1).getDay();

        $('#hp-current-month-year').text(
            currentDate.toLocaleDateString('en-US', { month: 'long', year: 'numeric' })
        );

        var dayNames = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'];
        var html = '';
        dayNames.forEach(function(d) { html += '<div class="hp-calendar-day-header">' + d + '</div>'; });

        for (var i = 0; i < startingDay; i++) {
            html += '<div class="hp-calendar-day other-month"></div>';
        }

        for (var day = 1; day <= daysInMonth; day++) {
            var dateStr = year + '-' + String(month + 1).padStart(2,'0') + '-' + String(day).padStart(2,'0');
            var isToday = isTodayDate(year, month, day);
            var dayBlocks   = getBlocksForDate(dateStr);
            var dayBookings = getBookingsForDate(dateStr);

            var cls = 'hp-calendar-day';
            if (isToday) cls += ' today';
            if (dayBlocks.length)   cls += ' has-blocks';
            if (dayBookings.length) cls += ' has-bookings';
            if (selectedDate === dateStr) cls += ' selected';

            var statusText = 'Available', statusClass = 'available';
            if (dayBlocks.length) {
                statusText = dayBlocks.length + ' block(s)';
                statusClass = 'blocked';
            } else if (dayBookings.length) {
                statusText = dayBookings.length + ' booking(s)';
                statusClass = 'booked';
            }

            html += '<div class="' + cls + '" data-date="' + dateStr + '">'
                  + '<div class="hp-day-number">' + day + '</div>'
                  + '<div class="hp-day-status ' + statusClass + '">' + statusText + '</div>'
                  + '</div>';
        }

        $('#hp-calendar-grid').html(html);
        $('.hp-calendar-day:not(.other-month)').on('click', function() {
            selectDate($(this).data('date'));
        });
    }

    function selectDate(date) {
        selectedDate = date;
        selectedTimeSlots = [];
        renderCalendar();
        showTimeSlots(date);
    }

    function showTimeSlots(date) {
        var dateObj = new Date(date + 'T00:00:00');
        $('#hp-selected-date').val(dateObj.toLocaleDateString('en-US', { weekday:'long', year:'numeric', month:'long', day:'numeric' }));
        $('#hp-selection-panel').show();
        $('#hp-use-custom-range').prop('checked', false);
        $('#hp-custom-range-wrap').hide();
        $('#hp-hours-warning').hide().text('');
        updateCustomTimePlaceholders(date);

        var slots = generateTimeSlots();
        var dateBlocks   = getBlocksForDate(date);
        var dateBookings = getBookingsForDate(date);
        var dateExtensions = getExtensionsForDate(date);

        if (!slots.length) {
            $('#hp-time-slots-container').html('<p class="hp-no-slots-msg">No time slots. Check the slot interval setting above.</p>');
            updateUnblockButton(date, dateBlocks, dateBookings, dateExtensions);
            return;
        }

        var slotsHtml = '<div class="hp-time-slots">';
        slots.forEach(function(slot) {
            var slotTime = slot + ':00';
            var blocked  = isTimeBlocked(date, slotTime, dateBlocks);
            var booked   = isTimeBooked(date, slotTime, dateBookings);
            var extended = isTimeExtended(date, slotTime, dateExtensions);
            var defaultBlocked = isTimeDefaultBlocked(slot);
            var sel      = selectedTimeSlots.indexOf(slot) > -1;
            var cls = 'hp-time-slot';
            if (blocked) cls += ' blocked';
            else if (booked) cls += ' booked';
            else if (defaultBlocked && !extended) cls += ' default-blocked';
            else if (extended) cls += ' extended';
            if (sel) cls += ' selected';
            slotsHtml += '<div class="' + cls + '" data-time="' + slot + '">' + slot + '</div>';
        });
        slotsHtml += '</div>';

        $('#hp-time-slots-container').html(slotsHtml);
        updateUnblockButton(date, dateBlocks, dateBookings, dateExtensions);

        $('.hp-time-slot:not(.blocked):not(.booked)')
            .on('mousedown', function(e) {
                isDragging = true;
                e.preventDefault();
                var time = $(this).data('time');
                dragMode = selectedTimeSlots.indexOf(time) === -1;
                setTimeSlotState(time, dragMode, this);
            })
            .on('mouseenter', function() {
                if (isDragging) {
                    var time = $(this).data('time');
                    setTimeSlotState(time, dragMode, this);
                }
            });
        $('.hp-time-slot.blocked:not(.booked), .hp-time-slot.default-blocked')
            .on('mousedown', function(e) {
                e.preventDefault();
                var time = $(this).data('time');
                var idx = selectedTimeSlots.indexOf(time);
                if (idx === -1) {
                    selectedTimeSlots.push(time);
                    selectedTimeSlots.sort();
                    $(this).addClass('selected');
                } else {
                    selectedTimeSlots.splice(idx, 1);
                    $(this).removeClass('selected');
                }
                updateUnblockButton(date, dateBlocks, dateBookings, dateExtensions);
            });
    }

    function updateCustomTimePlaceholders(date) {
        var slots = generateTimeSlots();
        if (slots.length) {
            $('#hp-custom-start').attr('min', slots[0]);
            $('#hp-custom-start').attr('max', slots[slots.length-1]);
            $('#hp-custom-end').attr('min', slots[0]);
            $('#hp-custom-end').attr('max', slots[slots.length-1]);
        }
    }

    function updateUnblockButton(date, dateBlocks, dateBookings, dateExtensions) {
        if (!selectedTimeSlots.length) {
            $('#hp-unblock-btn').hide();
            return;
        }
        dateExtensions = dateExtensions || [];
        var allBlocked = selectedTimeSlots.every(function(slot) {
            var slotTime = slot + ':00';
            return isTimeBlocked(date, slotTime, dateBlocks) && !isTimeBooked(date, slotTime, dateBookings);
        });
        var allDefaultBlocked = selectedTimeSlots.every(function(slot) {
            var slotTime = slot + ':00';
            return isTimeDefaultBlocked(slot) && !isTimeExtended(date, slotTime, dateExtensions) && !isTimeBooked(date, slotTime, dateBookings);
        });
        var show = allBlocked || allDefaultBlocked;
        $('#hp-unblock-btn').toggle(show).data('extend-mode', allDefaultBlocked && !allBlocked);
    }

    function setTimeSlotState(time, select, el) {
        var idx = selectedTimeSlots.indexOf(time);
        if (select && idx === -1) {
            selectedTimeSlots.push(time);
            selectedTimeSlots.sort();
            $(el).addClass('selected');
        } else if (!select && idx > -1) {
            selectedTimeSlots.splice(idx, 1);
            $(el).removeClass('selected');
        }
        if (selectedDate) {
            var dateBlocks = getBlocksForDate(selectedDate);
            var dateBookings = getBookingsForDate(selectedDate);
            var dateExtensions = getExtensionsForDate(selectedDate);
            updateUnblockButton(selectedDate, dateBlocks, dateBookings, dateExtensions);
        }
    }

    function generateTimeSlots() {
        var slots = [];
        var interval = parseInt(timeSlotInterval, 10);
        if (!interval || interval < 1) interval = 15;
        var sm = 0;
        var em = 24 * 60 - 1;
        for (var mins = sm; mins <= em; mins += interval) {
            var h = Math.floor(mins / 60);
            var m = mins % 60;
            if (h >= 24) break;
            slots.push(String(h).padStart(2,'0') + ':' + String(m).padStart(2,'0'));
        }
        return slots;
    }

    function parseHHMMToMinutes(s) {
        if (!s || typeof s !== 'string') return null;
        var m = s.match(/^(\d{2}):(\d{2})$/);
        if (!m) return null;
        var hh = parseInt(m[1], 10), mm = parseInt(m[2], 10);
        if (isNaN(hh) || isNaN(mm) || hh < 0 || hh > 23 || mm < 0 || mm > 59) return null;
        return (hh * 60) + mm;
    }

    function applyWorkingHours(saveToDb) {
        var st = $('#hp-working-start').val();
        var et = $('#hp-working-end').val();
        var iv = parseInt($('#hp-working-interval').val(), 10);
        if (!st || !et) {
            $('#hp-working-hours-status').show().text('Please set both Start and End.');
            return;
        }
        if (!iv || iv < 1) iv = 15;
        defaultStart = st;
        defaultEnd = et;
        timeSlotInterval = iv;
        $('#hp-working-hours-status').hide().text('');
        if (selectedDate) showTimeSlots(selectedDate);

        if (!saveToDb) return;
        $('#hp-working-hours-status').show().text('Saving…');
        $.post(phpBookingAdmin.ajaxUrl, {
            action: 'hp_save_availability_working_hours',
            nonce: phpBookingAdmin.nonce,
            working_hours_start: st,
            working_hours_end: et,
            time_slot_interval: iv
        }).done(function(r) {
            if (r && r.success) {
                $('#hp-working-hours-status').show().text('Saved.');
                setTimeout(function() { $('#hp-working-hours-status').fadeOut(200); }, 1200);
            } else {
                $('#hp-working-hours-status').show().text((r && r.data && r.data.message) ? r.data.message : 'Save failed.');
            }
        }).fail(function() {
            $('#hp-working-hours-status').show().text('Save failed.');
        });
    }

