    // Helpers
    // =========================================================
    function getBlocksForDate(date) {
        return blocks.filter(function(b) { return b.start_datetime.split(' ')[0] === date; });
    }
    function getExtensionsForDate(date) {
        return extensions.filter(function(e) { return e.date === date; });
    }
    function isTimeDefaultBlocked(slot) {
        var m = parseHHMMToMinutes(slot);
        if (m === null) return true;
        var ds = parseHHMMToMinutes(defaultStart);
        var de = parseHHMMToMinutes(defaultEnd);
        if (ds === null || de === null) return false;
        return m < ds || m >= de;
    }
    function isTimeExtended(date, time, list) {
        var t = time.substring(0, 5);
        return list.some(function(e) {
            var es = String(e.start_time).substring(0, 5);
            var ee = String(e.end_time).substring(0, 5);
            return t >= es && t < ee;
        });
    }
    function getBookingsForDate(date) {
        return bookings.filter(function(b) {
            return b.pickup_datetime.split(' ')[0] === date && b.booking_status !== 'cancelled';
        });
    }
    function isTimeBlocked(date, time, list) {
        return list.some(function(b) {
            return time >= b.start_datetime.split(' ')[1].substring(0,5)
                && time <  b.end_datetime.split(' ')[1].substring(0,5);
        });
    }
    function isTimeBooked(date, time, list) {
        return list.some(function(b) {
            return b.pickup_datetime.split(' ')[1].substring(0,5) === time;
        });
    }
    function isTodayDate(y, m, d) {
        var t = new Date();
        return t.getFullYear() === y && t.getMonth() === m && t.getDate() === d;
    }
    function clearSelection() {
        selectedDate = null;
        selectedTimeSlots = [];
        $('#hp-selection-panel').hide();
        renderCalendar();
    }
